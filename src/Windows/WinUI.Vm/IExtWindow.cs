namespace WinUI.Vm
{
    using System;

    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Interface for extent window. </summary>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public interface IExtWindow
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Size to content. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void SizeToContent();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the handle. </summary>
        ///
        /// <value> The handle. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public IntPtr Handle { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Hides the window 32 non client area. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void HideWin32NonClientArea();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Shows the window 32 non client area. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void ShowWin32NonClientArea();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Maximizes this.  </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void Maximize();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Minimizes this.  </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void Minimize();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Restores this.  </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void Restore();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Resizes. </summary>
        ///
        /// <param name="width">    The width. </param>
        /// <param name="height">   The height. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void Resize(int width,int height);

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Sets a transparency. </summary>
        ///
        /// <param name="percentTransparent">   The percent transparent. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void SetTransparency(int percentTransparent);

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Removes the transparency. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void RemoveTransparency();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Closes this.  </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void Close();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Enables the automatic scale on DPI change. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void EnableAutoScaleOnDpiChange();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Disables the automatic scale on DPI change. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void DisableAutoScaleOnDpiChange();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Occurs when Window State Changed. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        event EventHandler<EnumWindowState> WindowStateChanged;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Hides the custom non client area. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void HideCustomNonClientArea();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Shows the custom non client area. </summary>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void ShowCustomNonClientArea();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets window view model. </summary>
        ///
        /// <returns>   The window view model. </returns>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        WindowVm? GetWindowVm();

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Sets window view model. </summary>
        ///
        /// <param name="windowVm">    The window view model. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void SetWindowVm(WindowVm windowVm);

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Shows the cursor. </summary>
        ///
        /// <param name="makeVisible">  True to show, false to hide the make. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        void ShowCursor(bool makeVisible);
    }
}
